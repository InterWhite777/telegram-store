let cart = [];

function addToCart(name, price) {
  const existing = cart.find(item => item.name === name);
  if (existing) {
    existing.quantity += 1;
  } else {
    cart.push({ name, price, quantity: 1 });
  }
  alert(`‚úÖ –î–æ–±–∞–≤–ª–µ–Ω–æ: ${name}`);
}


function updateCartUI() {
  const cartList = document.getElementById("cart-items");
  const cartTotal = document.getElementById("cart-total");
  cartList.innerHTML = "";

  let total = 0;

  cart.forEach(item => {
    const itemText = `${item.name} x${item.quantity} = ${item.price * item.quantity}‚ÇΩ`;
    const li = document.createElement("li");
    li.textContent = itemText;
    cartList.appendChild(li);
    total += item.price * item.quantity;
  });

  cartTotal.textContent = `üí∞ –ò—Ç–æ–≥–æ: ${total}‚ÇΩ`;
}


function submitOrder() {
  if (cart.length === 0) {
    alert("–ö–æ—Ä–∑–∏–Ω–∞ –ø—É—Å—Ç–∞!");
    return;
  }

  let total = 0;
  cart.forEach(item => total += item.price * item.quantity);

  const data = {
    items: cart,
    total: total
  };

  if (window.Telegram.WebApp) {
    window.Telegram.WebApp.sendData(JSON.stringify(data));
    alert("‚úÖ –ó–∞–∫–∞–∑ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω!");
    cart = [];
    backToMain();
  } else {
    alert("‚ùå –û—à–∏–±–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏!");
  }
}





function openSection(section) {
  const sections = ["main", "store-section", "cart-section", "profile-section"];

  sections.forEach(id => {
    const el = document.getElementById(id);
    if (el) el.classList.add("hidden");
  });

  document.getElementById(`${section}-section`).classList.remove("hidden");

  if (section === "cart") updateCartUI();
  if (section === "profile") loadProfileInfo();
}

function backToMain() {
  openSection("store");
}


function removeFromCart(index) {
  cart.splice(index, 1);
  updateCartUI();
}

